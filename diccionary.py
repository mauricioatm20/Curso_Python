#une dos diccionarios

# un diccionario es una estructura de datos que permite almacenar pares de clave-valor.


diccionario = {'a':1 , 'b':2}
diccionario2 = {'c':3, 'd':4}


#El método update() en Python se utiliza para actualizar un diccionario añadiendo los elementos de
# otro diccionario o de una secuencia de pares clave-valor
diccionario.update(diccionario2)

print(diccionario)